package Serialization;

public class Product {
	 private String name;
	 private Data data;
	    public Product() {
	    }
	    public String getName() {
	        return name;
	    }
	    public void setName(String name) {
	        this.name = name;
	    }
	    public Data getData() {
	        return data;
	    }
	    public void setData(Data data) {
	        this.data = data;
	    }
}
